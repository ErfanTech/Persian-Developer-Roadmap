    <h1>Semantic HTML</h1>
    <h2>مطالعه معانی HTML</h2>
    <br>
    <p>
        کلمه ی semantics به معنای study of meaning یا «مطالعه ی معانی» است که از شاخه ی زبانشناسی وارد حوزه های دیگر شده
        است. با اینکه نسخه های قبل از HTML5 نیز عناصر معنایی داشتند (مانند تگ body یا html) اما تنها با معرفی نسخه ی 5
        زبان HTML بود که مفهوم semantics در HTML در مرکز توجه قرار گرفت.
        همانطور که می‌دانید تگ های HTML معنای خاصی دارند؛ مثلا تگ های <em>&lt;h1&gt;</em>,&nbsp;<em>&lt;h2&gt;</em>,&nbsp;<em>&lt;h3&gt;</em>,&nbsp;<em>&lt;h4&gt;</em>,&nbsp;<em>&lt;h5&gt;</em>,&nbsp;<em>&lt;h6&gt;</em>
        هر کدام بیانگر میزان اهمیت یک تیتر و نمایش دهنده ی ساختار HTML اند. لیست های ترتیبی و غیرترتیبی
        (<em>&lt;ol&gt;</em>,&nbsp;<em>&lt;ul&gt;</em>) نیز از همین دسته اند. با اینکه کاربر ممکن است به خاطر وجود CSS
        هر دو را به یک شکل ببیند اما موتورهای جست و جو هنوز هم این تگ ها را به همراه معنی شان می خوانند.
