        مدل‌های جدید توسعه برنامه‌های JS، تمرکز بر ساخت اجزایی به نام کامپوننت است و ایده اصلی این پارادایم، نوشتن و
        نگهداری کدهای CSS در کنار کدهای JS استفاده کننده از آن می‌باشد.
        با استفاده از این روش کامپوننت‌های تولید شده علاوه بر کپسوله‌سازی روال‌های منطقی در درون خود، دارای استایل مناسب
        هستند و در صورت بکارگیری آن‌ها در پروژه‌های دیگر، نیاز به بارگذاری فایل‌های CSS مرتبط با آن‌ها به صورت مجزا وجود
        ندارد.
        به طور خلاصه این پارادایم به این معنی است که فایل‌های CSS که در آن‌ها نام کلاس‌ها و نام‌های انیمیشن به‌طور
        پیش‌فرض به صورت محلی درج شده‌اند.
