    <p>
        DOM مخفف Document Object Model است و یک رابط برنامه نویسی است که به ما امکان ایجاد، تغییر یا حذف عناصر از سند
        HTML را می‌دهد. ما می‌توانیم رویدادهایی را نیز به این عناصر اضافه کنیم تا صفحه وب خود را پویاتر کنیم.
        DOM مخفف عبارت Document Object Model به معنی مدل شی‌گرای سند است. این استاندارد را کنسرسیوم جهانی وب (W3C) تعریف
        کرده است. در واقع DOM یک رابط برنامه نویسی یا API است که به ما امکان ایجاد، تغییر یا حذف عناصر از اسناد HTML،
        XML و XHTML را می‌دهد. همچنین از طریق DOM می‌توانیم رویدادهایی را به این عناصر اضافه کنیم تا صفحه وب خود را
        پویاتر کنیم.
